public class App {
    public static void main(String[] args) {
        // println print output dengan menambahkan enter di posisi terakhir
        System.out.println("Halo dunia!");
        System.out.println("baris kedua : Halo juga mas owi!!");

        // print : print tanpa penambahan enter / newline
        System.out.print("baris ketiga : saya adalah baris ketiga");
        System.out.print("baris keempat : saya anak baris ketiga");

        System.out.print("baris kelima : saya anak kelima");
        System.out.println("baris keenam : yuk main yuk!");

        System.out.print("baris ketujuh : aku pasti dibawah\n");
        // print enter dengan \n
        System.out.print("baris kedelapan : halo yaouu\n");
        System.out.print("baris kesembilan : aku dibawahnya delapan\n");

        // printf : kosep hampir sama dengan printf di C
        System.out.printf("Aku adalah owi, umur %d tahun", 19);
    }
}
